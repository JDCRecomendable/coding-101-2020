// Here we define our function
function calculateAverage(firstNumber, secondNumber) {
    // This sort of average is known as an "arithmetic mean"
    return ((firstNumber + secondNumber) / 2)
}

// Here we call our calculateAverage function with arguments of 5 and 10
let averageValue = calculateAverage(5, 10)  // (5+10)/2 = 15/2 = 7.5
console.log(averageValue)

